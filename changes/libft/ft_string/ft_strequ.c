#include "ft_stdlib.h"
#include "ft_string.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;

	i = 0;
	if (s1 != NULL && s2 != NULL)
		while ((s1[i] != '\0') || (s2[i] != '\0'))
		{
			if (s1[i] - s2[i] != 0)
				return (0);
			i++;
		}
	return (1);
}
